package koreait.day01;


public class C01_print {
	
	//프로그램 실행 - main 메소드가 필요합니다.	
	public static void main(String[] args) {
		
		
		
		System.out.println(); //화면에 출력
		System.out.println("하이 !!"); //문자열 데이터
		System.out.println(3.14);	//실수 데이터
		System.out.println(100);	//정수 데이터
		System.out.println(34+567);	//수식(덧셈)
		System.out.println(true);	//논리 (boolean) 데이터
		System.out.println("하이!!"   +   "자바야~");	//문자열의 + 기능? 연결
		System.out.println("나는 "+ 23 + "세 입니다. "); //문자열 + 정수 ?   연걸
//		ln이 있으면 줄바꿈, print () 메소드는 줄바꿈 안합니다.
		

	}
}


